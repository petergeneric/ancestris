    public void increment() {
        if (!boxStep1.isSelected()) { 
            boxStep1.setSelected(true);
            icoStep1.setVisible(false);
            icoStep2.setVisible(true);
            return;
        }
        if (!boxStep2.isSelected()) { 
            boxStep2.setSelected(true);
            icoStep2.setVisible(false);
            icoStep3.setVisible(true);
            return;
        }
        if (!boxStep3.isSelected()) { 
            boxStep3.setSelected(true);
            icoStep3.setVisible(false);
            icoStep4.setVisible(true);
            return;
        }
        if (!boxStep4.isSelected()) { 
            boxStep4.setSelected(true);
            icoStep4.setVisible(false);
            icoStep5.setVisible(true);
            return;
        }
        if (!boxStep5.isSelected()) { 
            boxStep5.setSelected(true);
            icoStep5.setVisible(false);
            return;
        }
    }

