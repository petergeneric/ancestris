    void setUndos(String str) {
        if (str.equals("-1")) {
            str = "10";
        }
        Integer i = getIntFromStr(str);
        if (i == -1) {
            i = 10;
        }
        if (i > 300) {
            i = 300;
        }
        jSpinner1.setValue(i);
    }

    String getUndos() {
        return jSpinner1.getValue().toString();
    }

