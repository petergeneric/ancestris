    /**
     * LOC12_SURN_LOC34 = 0;
     * LOC12_LOC34_SURN = 1;
     * SURN_LOC12_LOC34 = 2;
     * @return 
     */
    public int getRecordKey() {
        int surn = 0;
        for (int r = 0; r < MAXROWS; r++) {
            if (!rows[r].isLocVisible) {
                surn = r;
                break;
            }
        }
        if (surn == 0) {
            return 2;
        }
        if (surn == 1) {
            return 0;
        }
        if (surn == 2) {
            return 1;
        }
        return 0;
    }

    /**
     * LOC12_SURN_LOC34 = 0;
     * LOC12_LOC34_SURN = 1;
     * SURN_LOC12_LOC34 = 2;
     * @return 
     */
    public int getPosLoc(int i) {
        int ret = -1;
        int rk = getRecordKey();
        switch (i) {
            case 1:
                if (rk != 2) {
                    ret = location1ComboBox1.getSelectedIndex();
                } else {
                    ret = location2ComboBox1.getSelectedIndex();
                }
                break;
            case 2:
                if (rk != 2) {
                    ret = location1ComboBox2.getSelectedIndex() - 1;
                } else {
                    ret = location2ComboBox2.getSelectedIndex() - 1;
                }
                break;
            case 3:
                if (rk != 1) {
                    ret = location3ComboBox1.getSelectedIndex();
                } else {
                    ret = location2ComboBox1.getSelectedIndex();
                }
                break;
            case 4:
                if (rk != 1) {
                    ret = location3ComboBox2.getSelectedIndex() - 1;
                } else {
                    ret = location2ComboBox2.getSelectedIndex() - 1;
                }
                break;
        }
        return ret;
    }

    
    
