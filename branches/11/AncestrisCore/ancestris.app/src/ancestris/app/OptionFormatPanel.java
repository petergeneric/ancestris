    void setImageSize(String str) {
        if (str.equals("-1")) {
            str = "128";
        }
        Integer i = getIntFromStr(str);
        if (i == -1) {
            i = 128;
        }
        if (i > 16384) {
            i = 16384;
        }
        jSpinner1.setValue(i);
    }

    String getImageSize() {
        return jSpinner1.getValue().toString();
    }

    private Integer getIntFromStr(String str) {

        Integer i = 0;
        try {
            i = Integer.valueOf(str);
        } catch (Exception e) {
            i = -1;
        }
        return i;
    }
