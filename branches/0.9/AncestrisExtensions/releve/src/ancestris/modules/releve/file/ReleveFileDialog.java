    public static int show(java.awt.Frame parent, String title, String result, String error) {
        final ReleveFileDialog dialog = new ReleveFileDialog(parent);
        dialog.setTitle(title);
        dialog.setModal(true);
        dialog.jTextAreaResult.setText(result);
        dialog.jTextAreaDetail.setText(error);
        dialog.setLocationRelativeTo(parent);
        int resultButton = 0;

        dialog.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
                    public void windowClosing(java.awt.event.WindowEvent e) {
                        dialog.setVisible(false);
                    }
                });
        dialog.setVisible(true);
        return resultButton;
    }

