    public PropertyName getNameToEdit() {
        return nameToEdit;
    }

    /**
     * @param name the name to set
     */
    public void set(PropertyName name) {
        this.nameToEdit = name;
        update();
    }

    private void update() {
        Property nameType = nameToEdit.getProperty("TYPE");
        if (nameType != null) {
            nameTypeComboBox.setSelectedIndex(1);
        } else {
            nameTypeComboBox.setSelectedIndex(1);
        }

        Property namePrefix = nameToEdit.getProperty("NPFX");
        firstNamePrefixTextField.setText(namePrefix != null ? namePrefix.getValue() : "");

        Property givenName = nameToEdit.getProperty("GIVN");
        if (givenName != null) {
            firstNameTextField.setText(givenName.getValue());
        } else {
            firstNameTextField.setText(nameToEdit.getFirstName());
        }

        Property nameSuffix = nameToEdit.getProperty("NSFX");
        firstNameSuffixTextField.setText(nameSuffix != null ? nameSuffix.getValue() : "");

        Property nameNickname = nameToEdit.getProperty("NICK");
        nicknameTextField.setText(nameNickname != null ? nameNickname.getValue() : "");

        Property surname = nameToEdit.getProperty("SURN");
    }
