        if (individualsCheckBox.isSelected()) {
            getSelectedEntities().add(Gedcom.INDI);
            cl.show(jPanel7, "individualsOptions");
        } else {
            getSelectedEntities().remove(Gedcom.INDI);
            cl.show(jPanel7, "emptyPanel");
        }
